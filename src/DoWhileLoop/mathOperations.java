package DoWhileLoop;

public class mathOperations {
    int add (int a, int b){
        return a + b;
    }

    int add ( int a, int b, int c){
        return a + b + c;
    }
    double add ( double a, double b){
        return a + b;
    }
    double add (double a, double b, double c, double m){
        return a + b + c + m;
    }
}
